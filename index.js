// Addition
function add(x, y){
  return x + y
}

//Subtraction
function subtract(x, y){
  return x - y
}

//multiplicatoin
function multiply(x, y){
  return x * y
}

//division
function divide(x, y){
  return x / y
}

//increment
function increment(n){
  return ++n
}

//decrement
function decrement(n){
  --n
}

//makeInt
function makeInt(n){
  return parseInt(n, 10)
}
